//Area do círculo
// A = pi * (r*r)

// Declaração da variável 'pi' e atribuição do valor 3.14 a ela
let pi = 3.14;

// Declaração de uma função anônima chamada 'f'
// A função aceita um parâmetro 'r', que representa o raio do círculo
let f = (r) => pi * (r * r);

// Chamada da função 'f' com o argumento 10 e impressão do resultado no console
console.log(f(10));
